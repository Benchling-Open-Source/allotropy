"""Code generator: convert JSON Schema definitions to Python dataclass modules.

Generates one Python module per JSON schema file, with proper cross-module
imports based on $ref relationships.
"""

from __future__ import annotations

import re
from collections import defaultdict
from dataclasses import dataclass, field
from typing import Any

from allotropy.schema_gen.naming import (
    def_name_to_class_name,
    normalize_schema_url,
    parse_ref,
    property_name_to_class_name,
    property_name_to_python,
    quantity_value_class_name,
    schema_url_to_module_path,
)


# Properties starting with these prefixes are schema metadata, not data fields
ASM_METADATA_PREFIXES = ("$asm.", "$schema", "$id", "$comment")


@dataclass
class ImportEntry:
    """A Python import to add to a generated module."""

    module: str  # Full module path (e.g., "allotropy.allotrope.models_v2.adm.core...")
    name: str  # Class/type name to import


@dataclass
class GeneratedClass:
    """A generated Python class or type alias."""

    name: str
    code: str
    is_type_alias: bool = False


@dataclass
class ModuleCode:
    """Complete generated code for a Python module."""

    schema_url: str
    imports: list[ImportEntry] = field(default_factory=list)
    classes: list[GeneratedClass] = field(default_factory=list)
    # Map from definition names in this schema to their Python class names
    exported_names: dict[str, str] = field(default_factory=dict)

    def render(self, models_package: str = "allotropy.allotrope.models_v2") -> str:
        """Render the complete Python module source code."""
        # Reorder classes so that dependencies come before uses
        self.classes = _topological_sort_classes(self.classes)

        lines: list[str] = []
        lines.append("# generated by allotropy.schema_gen")
        lines.append("")
        lines.append("from __future__ import annotations")
        lines.append("")

        # Collect and deduplicate imports
        stdlib_imports: set[str] = set()
        external_imports: dict[str, set[str]] = defaultdict(set)

        # Check all generated code for what imports are needed
        all_code = "\n".join(c.code for c in self.classes)
        has_dataclasses = any(not c.is_type_alias for c in self.classes)
        if has_dataclasses:
            stdlib_imports.add("from dataclasses import dataclass")
            if "field(" in all_code:
                stdlib_imports.add("from dataclasses import field")
        if "Literal[" in all_code:
            stdlib_imports.add("from typing import Literal")
        if "Any" in all_code:
            stdlib_imports.add("from typing import Any")

        for imp in self.imports:
            module = imp.module
            # Convert relative module path to full package path
            if not module.startswith("allotropy"):
                module = f"{models_package}.{module}"
            external_imports[module].add(imp.name)

        # Write stdlib imports
        for imp in sorted(stdlib_imports):
            lines.append(imp)
        if stdlib_imports:
            lines.append("")

        # Write external imports
        for module in sorted(external_imports.keys()):
            names = sorted(external_imports[module])
            if len(names) == 1:
                lines.append(f"from {module} import {names[0]}")
            else:
                lines.append(f"from {module} import (")
                for name in names:
                    lines.append(f"    {name},")
                lines.append(")")
        if external_imports:
            lines.append("")
        lines.append("")

        # Write classes and type aliases
        for cls in self.classes:
            lines.append(cls.code)
            lines.append("")
            lines.append("")

        # Remove trailing blank lines
        while lines and lines[-1] == "":
            lines.pop()
        lines.append("")  # Single trailing newline

        return "\n".join(lines)


def _topological_sort_classes(classes: list[GeneratedClass]) -> list[GeneratedClass]:
    """Sort classes so that dependencies (referenced names) come before uses.

    This ensures type aliases like `TNumericValue = TByteValue | TShortValue`
    appear after TByteValue and TShortValue are defined.
    """
    # Build a map of defined names
    name_to_class: dict[str, int] = {}
    for i, cls in enumerate(classes):
        name_to_class[cls.name] = i

    # For each class, find which other classes in this module it references
    deps: dict[int, set[int]] = {i: set() for i in range(len(classes))}
    for i, cls in enumerate(classes):
        code = cls.code
        for name, j in name_to_class.items():
            if i == j:
                continue
            # Check if this class references the other by name
            # Use word boundary to avoid false matches (e.g., "T" matching "TStringValue")
            if re.search(rf'\b{re.escape(name)}\b', code):
                deps[i].add(j)

    # Topological sort (Kahn's algorithm)
    in_degree = {i: len(d) for i, d in deps.items()}
    queue = [i for i, d in in_degree.items() if d == 0]
    result: list[int] = []

    while queue:
        idx = queue.pop(0)
        result.append(idx)
        for other, dep_set in deps.items():
            if idx in dep_set:
                in_degree[other] -= 1
                if in_degree[other] == 0:
                    queue.append(other)

    # Handle cycles (shouldn't happen but be safe)
    if len(result) != len(classes):
        remaining = set(range(len(classes))) - set(result)
        result.extend(remaining)

    return [classes[i] for i in result]


def _field_declaration(
    python_name: str, type_str: str, json_name: str, is_required: bool
) -> str:
    """Generate a dataclass field declaration with JSON name metadata.

    Stores the original JSON property name in field metadata so serialization
    can map back to schema property names.
    """
    metadata = f'{{"json_name": {json_name!r}}}'
    if is_required:
        return f"    {python_name}: {type_str} = field(metadata={metadata})"
    return f"    {python_name}: {type_str} | None = field(default=None, metadata={metadata})"


class SchemaCodeGenerator:
    """Generates Python code from a set of JSON schemas."""

    def __init__(
        self,
        schemas: dict[str, dict[str, Any]],
        generation_order: list[str],
        models_package: str = "allotropy.allotrope.models_v2",
    ) -> None:
        self.schemas = schemas
        self.generation_order = generation_order
        self.models_package = models_package
        # Track generated modules for cross-references
        self._modules: dict[str, ModuleCode] = {}
        # Track unit symbols and their const values for quantity value generation
        self._unit_symbols: dict[str, str] = {}  # def_name -> const_value

    def generate_all(self) -> dict[str, ModuleCode]:
        """Generate Python modules for all schemas in dependency order."""
        for url in self.generation_order:
            schema = self.schemas[url]
            module = self._generate_module(url, schema)
            self._modules[url] = module
        return self._modules

    def _generate_module(self, schema_url: str, schema: dict[str, Any]) -> ModuleCode:
        """Generate a Python module for a single schema."""
        module = ModuleCode(schema_url=schema_url)

        defs = schema.get("$defs", {})

        if self._is_units_schema(schema_url):
            self._generate_units_module(module, defs)
        elif self._is_adm_schema(schema_url, schema):
            self._generate_adm_module(module, schema_url, schema)
        else:
            # Regular schema (core, cube, hierarchy, manifest, detector)
            self._generate_defs_module(module, schema_url, defs)

        return module

    def _is_units_schema(self, url: str) -> bool:
        return "units.schema" in url

    def _is_adm_schema(self, url: str, schema: dict[str, Any]) -> bool:
        """Check if this is a top-level ADM schema (has allOf at root, not just $defs)."""
        return "allOf" in schema and "$defs" not in schema

    # -------------------------------------------------------------------------
    # Units schema generation
    # -------------------------------------------------------------------------

    def _generate_units_module(self, module: ModuleCode, defs: dict[str, Any]) -> None:
        """Generate the units module with a HasUnit base class and unit subclasses."""
        # Base class
        module.classes.append(GeneratedClass(
            name="HasUnit",
            code=(
                "@dataclass(frozen=True, kw_only=True)\n"
                "class HasUnit:\n"
                "    unit: str"
            ),
        ))
        module.exported_names["HasUnit"] = "HasUnit"

        used_class_names: set[str] = {"HasUnit"}
        for def_name, def_schema in defs.items():
            const_value = self._extract_unit_const(def_schema)
            if const_value is None:
                continue
            class_name = self._unit_class_name(def_name, const_value)
            self._unit_symbols[def_name] = const_value

            # Deduplicate class names
            if class_name in used_class_names:
                # Append a suffix from the def name to disambiguate
                suffix = re.sub(r"[^a-zA-Z0-9]", "", def_name)
                class_name = f"{class_name}_{suffix}"
            used_class_names.add(class_name)

            module.classes.append(GeneratedClass(
                name=class_name,
                code=(
                    f"@dataclass(frozen=True, kw_only=True)\n"
                    f"class {class_name}(HasUnit):\n"
                    f"    unit: str = {const_value!r}"
                ),
            ))
            module.exported_names[def_name] = class_name

    def _extract_unit_const(self, schema: dict[str, Any]) -> str | None:
        """Extract the const unit value from a unit definition."""
        props = schema.get("properties", {})
        unit_prop = props.get("unit", {})
        return unit_prop.get("const")

    def _unit_class_name(self, def_name: str, const_value: str) -> str:
        """Generate a class name for a unit definition."""
        from allotropy.schema_gen.naming import unit_symbol_to_class_name
        return unit_symbol_to_class_name(const_value)

    # -------------------------------------------------------------------------
    # Regular $defs module generation (core, cube, hierarchy, manifest, detector)
    # -------------------------------------------------------------------------

    def _generate_defs_module(
        self, module: ModuleCode, schema_url: str, defs: dict[str, Any]
    ) -> None:
        """Generate classes for all $defs in a schema."""
        for def_name, def_schema in defs.items():
            if not isinstance(def_schema, dict):
                continue
            class_name = def_name_to_class_name(def_name)
            code = self._generate_type(module, schema_url, class_name, def_schema)
            if code:
                module.classes.append(GeneratedClass(
                    name=class_name,
                    code=code,
                    is_type_alias="class " not in code,
                ))
                module.exported_names[def_name] = class_name

    def _generate_type(
        self,
        module: ModuleCode,
        schema_url: str,
        class_name: str,
        schema: dict[str, Any],
    ) -> str | None:
        """Generate Python code for a single type definition."""
        # Handle oneOf patterns (value types like tStringValue)
        if "oneOf" in schema:
            return self._generate_one_of(module, schema_url, class_name, schema)

        # Handle anyOf patterns (union types like tNumericValue, tOrderedValue)
        if "anyOf" in schema:
            return self._generate_any_of(module, schema_url, class_name, schema)

        # Handle enum types
        if "enum" in schema:
            return self._generate_enum(class_name, schema)

        # Handle object types with properties
        if schema.get("type") == "object" and "properties" in schema:
            return self._generate_dataclass(module, schema_url, class_name, schema)

        # Handle object type without properties (just a marker/base type)
        if schema.get("type") == "object":
            return (
                f"@dataclass(frozen=True, kw_only=True)\n"
                f"class {class_name}:\n"
                f"    pass"
            )

        # Handle simple type aliases
        if "type" in schema:
            python_type = self._json_type_to_python(schema["type"])
            return f"{class_name} = {python_type}"

        # Handle allOf at the definition level
        if "allOf" in schema:
            return self._generate_all_of_def(module, schema_url, class_name, schema)

        # Handle $ref at the top level of a def (alias)
        if "$ref" in schema:
            ref_type = self._resolve_ref_type(module, schema_url, schema["$ref"])
            return f"{class_name} = {ref_type}"

        # Handle dependencies/constraints (like tRangeValue)
        if "properties" in schema:
            return self._generate_dataclass(module, schema_url, class_name, schema)

        return None

    # -------------------------------------------------------------------------
    # oneOf: value types (primitive | typed object)
    # -------------------------------------------------------------------------

    def _generate_one_of(
        self,
        module: ModuleCode,
        schema_url: str,
        class_name: str,
        schema: dict[str, Any],
    ) -> str:
        """Generate a type for a oneOf schema.

        Common pattern: oneOf[primitive_type, {type: object, properties: {value, @type}}]
        → Generate both the typed object class and a union alias.
        """
        one_of = schema["oneOf"]

        # Check for the common "primitive | typed object" pattern
        primitive_types: list[str] = []
        object_schemas: list[dict[str, Any]] = []
        ref_types: list[str] = []

        for variant in one_of:
            if "$ref" in variant:
                ref_type = self._resolve_ref_type(module, schema_url, variant["$ref"])
                ref_types.append(ref_type)
            elif "properties" in variant:
                object_schemas.append(variant)
            elif "type" in variant:
                python_type = self._json_type_to_python(variant["type"])
                primitive_types.append(python_type)
            elif "format" in variant:
                primitive_types.append("str")
            elif "required" in variant and not any(k not in ("required",) for k in variant):
                pass  # Skip constraint-only variants (e.g., oneOf[{required: [a]}, {required: [b]}])

        parts: list[str] = []

        # If there's a primitive + object variant, generate a typed item class
        if primitive_types and object_schemas:
            item_class_name = f"{class_name}Item"
            item_code = self._generate_dataclass(
                module, schema_url, item_class_name, object_schemas[0]
            )
            if item_code:
                module.classes.append(GeneratedClass(name=item_class_name, code=item_code))
                module.exported_names[f"{class_name}_item"] = item_class_name
            parts.extend(primitive_types)
            parts.append(item_class_name)
        elif primitive_types:
            parts.extend(primitive_types)
        elif object_schemas:
            if len(object_schemas) == 1:
                # Single object variant - generate as dataclass
                return self._generate_dataclass(module, schema_url, class_name, object_schemas[0])
            # Multiple object variants (e.g., tMeasureData) - merge all properties
            merged_props: dict[str, Any] = {}
            for obj_schema in object_schemas:
                merged_props.update(obj_schema.get("properties", {}))
            merged = {"type": "object", "properties": merged_props}
            return self._generate_dataclass(module, schema_url, class_name, merged)

        # Add ref types
        parts.extend(ref_types)

        if not parts:
            return f"{class_name} = Any"
        if len(parts) == 1:
            return f"{class_name} = {parts[0]}"
        return f"{class_name} = {' | '.join(parts)}"

    # -------------------------------------------------------------------------
    # anyOf: union types
    # -------------------------------------------------------------------------

    def _generate_any_of(
        self,
        module: ModuleCode,
        schema_url: str,
        class_name: str,
        schema: dict[str, Any],
    ) -> str:
        """Generate a Union type for an anyOf schema."""
        any_of = schema["anyOf"]
        parts: list[str] = []
        for variant in any_of:
            if "$ref" in variant:
                ref_type = self._resolve_ref_type(module, schema_url, variant["$ref"])
                parts.append(ref_type)
            elif "type" in variant:
                parts.append(self._json_type_to_python(variant["type"]))

        if len(parts) == 1:
            return f"{class_name} = {parts[0]}"
        return f"{class_name} = {' | '.join(parts)}"

    # -------------------------------------------------------------------------
    # enum types
    # -------------------------------------------------------------------------

    def _generate_enum(self, class_name: str, schema: dict[str, Any]) -> str:
        """Generate a Literal type for an enum schema."""
        values = schema["enum"]
        if all(isinstance(v, str) for v in values):
            literals = ", ".join(repr(v) for v in values)
            return f"{class_name} = Literal[{literals}]"
        # Fallback for non-string enums
        literals = ", ".join(repr(v) for v in values)
        return f"{class_name} = Literal[{literals}]"

    # -------------------------------------------------------------------------
    # allOf at definition level
    # -------------------------------------------------------------------------

    def _generate_all_of_def(
        self,
        module: ModuleCode,
        schema_url: str,
        class_name: str,
        schema: dict[str, Any],
    ) -> str:
        """Generate a type for an allOf at the definition level."""
        all_of = schema["allOf"]
        # Merge all properties from allOf items
        merged_props: dict[str, Any] = {}
        merged_required: list[str] = []
        base_classes: list[str] = []

        for item in all_of:
            if "$ref" in item:
                ref_type = self._resolve_ref_type(module, schema_url, item["$ref"])
                base_classes.append(ref_type)
            if "properties" in item:
                merged_props.update(item["properties"])
            if "required" in item:
                merged_required.extend(item["required"])

        if not merged_props and base_classes:
            # Pure allOf of refs → multiple inheritance
            bases = ", ".join(base_classes)
            return (
                f"@dataclass(frozen=True, kw_only=True)\n"
                f"class {class_name}({bases}):\n"
                f"    pass"
            )

        # Build merged schema for dataclass generation
        merged = {"type": "object", "properties": merged_props}
        if merged_required:
            merged["required"] = merged_required
        return self._generate_dataclass(
            module, schema_url, class_name, merged, base_classes=base_classes
        )

    # -------------------------------------------------------------------------
    # Object → dataclass generation
    # -------------------------------------------------------------------------

    def _generate_dataclass(
        self,
        module: ModuleCode,
        schema_url: str,
        class_name: str,
        schema: dict[str, Any],
        *,
        base_classes: list[str] | None = None,
        frozen: bool = True,
    ) -> str:
        """Generate a frozen dataclass from an object schema."""
        properties = schema.get("properties", {})
        required = set(schema.get("required", []))

        lines: list[str] = []
        frozen_str = "frozen=True, " if frozen else ""
        bases = f"({', '.join(base_classes)})" if base_classes else ""
        lines.append(f"@dataclass({frozen_str}kw_only=True)")
        lines.append(f"class {class_name}{bases}:")

        required_fields: list[str] = []
        optional_fields: list[str] = []
        for prop_name, prop_schema in properties.items():
            # Skip ASM metadata properties
            if any(prop_name.startswith(p) for p in ASM_METADATA_PREFIXES):
                continue

            python_name = property_name_to_python(prop_name)
            type_str = self._resolve_property_type(
                module, schema_url, prop_name, prop_schema
            )
            is_required = prop_name in required
            field_line = _field_declaration(python_name, type_str, prop_name, is_required)

            if is_required:
                required_fields.append(field_line)
            else:
                optional_fields.append(field_line)

        all_fields = required_fields + optional_fields
        if all_fields:
            lines.extend(all_fields)
        else:
            lines.append("    pass")

        return "\n".join(lines)

    # -------------------------------------------------------------------------
    # Property type resolution
    # -------------------------------------------------------------------------

    def _resolve_property_type(
        self,
        module: ModuleCode,
        schema_url: str,
        prop_name: str,
        prop_schema: dict[str, Any],
    ) -> str:
        """Determine the Python type for a property schema."""
        # Strip $asm metadata from the schema copy
        prop_schema = {
            k: v for k, v in prop_schema.items()
            if not any(k.startswith(p) for p in ASM_METADATA_PREFIXES)
        }

        # Direct $ref
        if "$ref" in prop_schema and len(prop_schema) == 1:
            return self._resolve_ref_type(module, schema_url, prop_schema["$ref"])

        # $ref with additional constraints (e.g., $ref + required)
        if "$ref" in prop_schema:
            return self._resolve_ref_type(module, schema_url, prop_schema["$ref"])

        # allOf pattern - check for quantity value + unit
        if "allOf" in prop_schema:
            return self._resolve_all_of_property(module, schema_url, prop_name, prop_schema)

        # anyOf - check for detector measurement items pattern
        if "anyOf" in prop_schema:
            return self._resolve_any_of_property(module, schema_url, prop_name, prop_schema)

        # oneOf with properties: oneOf is just validation constraints, generate from properties
        if "oneOf" in prop_schema and "properties" in prop_schema:
            inline_class_name = property_name_to_class_name(prop_name)
            code = self._generate_dataclass(module, schema_url, inline_class_name, prop_schema)
            if code:
                module.classes.append(GeneratedClass(name=inline_class_name, code=code))
            return inline_class_name

        # oneOf
        if "oneOf" in prop_schema:
            return self._resolve_one_of_property(module, schema_url, prop_name, prop_schema)

        # Array type
        if prop_schema.get("type") == "array":
            return self._resolve_array_type(module, schema_url, prop_name, prop_schema)

        # Inline object (with or without explicit type: "object")
        if "properties" in prop_schema:
            inline_class_name = property_name_to_class_name(prop_name)
            code = self._generate_dataclass(module, schema_url, inline_class_name, prop_schema)
            if code:
                module.classes.append(GeneratedClass(name=inline_class_name, code=code))
            return inline_class_name

        # Enum/const
        if "enum" in prop_schema:
            values = prop_schema["enum"]
            literals = ", ".join(repr(v) for v in values)
            return f"Literal[{literals}]"
        if "const" in prop_schema:
            return f"Literal[{prop_schema['const']!r}]"

        # Simple types
        if "type" in prop_schema:
            return self._json_type_to_python(prop_schema["type"])

        # Format-based type
        if "format" in prop_schema:
            return "str"

        # Schema with only `required` constraint (constraint overlay, no new type info)
        if set(prop_schema.keys()) <= {"required", "type"}:
            return "dict[str, Any]"

        return "Any"

    def _resolve_all_of_property(
        self,
        module: ModuleCode,
        schema_url: str,
        prop_name: str,
        prop_schema: dict[str, Any],
    ) -> str:
        """Resolve an allOf property type.

        Key patterns:
        1. allOf[tQuantityValue, unit_ref] → TQuantityValue{Unit}
        2. allOf[tClass, {enum: [...]}] → Literal[...]
        3. allOf[ref, {properties: ...}] → merged inline class
        4. allOf[{properties: ...}, orderedItem] → inline class with index
        """
        all_of = prop_schema["allOf"]

        refs: list[str] = []
        schemas: list[dict[str, Any]] = []
        for item in all_of:
            if "$ref" in item:
                refs.append(item["$ref"])
            elif isinstance(item, dict):
                schemas.append(item)

        # Pattern 1: tQuantityValue + unit
        quantity_ref = None
        unit_ref = None
        for ref in refs:
            _, def_name = parse_ref(ref)
            if def_name and def_name.lower() in ("tquantityvalue", "tquantityvalue"):
                quantity_ref = ref
            elif def_name:
                ref_schema_url = ref.split("#")[0]
                try:
                    canonical = normalize_schema_url(ref_schema_url) if ref_schema_url else None
                except ValueError:
                    canonical = None
                if canonical and "units.schema" in canonical:
                    unit_ref = ref

        if quantity_ref and unit_ref:
            return self._generate_quantity_value_type(module, schema_url, quantity_ref, unit_ref)

        # Pattern 2: tClass + enum constraint
        class_ref = None
        enum_values = None
        for ref in refs:
            _, def_name = parse_ref(ref)
            if def_name and def_name == "tClass":
                class_ref = ref
        for s in schemas:
            if "enum" in s:
                enum_values = s["enum"]

        if class_ref and enum_values:
            literals = ", ".join(repr(v) for v in enum_values)
            return f"Literal[{literals}]"

        # Pattern 3/4: Merge allOf into an inline class
        merged_props: dict[str, Any] = {}
        merged_required: list[str] = []
        base_refs: list[str] = []

        for item in all_of:
            if "$ref" in item:
                base_refs.append(item["$ref"])
            if isinstance(item, dict):
                if "properties" in item:
                    merged_props.update(item["properties"])
                if "required" in item:
                    merged_required.extend(item["required"])

        # If we have properties, generate an inline class
        if merged_props:
            inline_class_name = property_name_to_class_name(prop_name)
            base_classes = [
                self._resolve_ref_type(module, schema_url, ref) for ref in base_refs
            ]
            merged = {"type": "object", "properties": merged_props}
            if merged_required:
                merged["required"] = merged_required
            code = self._generate_dataclass(
                module, schema_url, inline_class_name, merged, base_classes=base_classes
            )
            if code:
                module.classes.append(GeneratedClass(name=inline_class_name, code=code))
            return inline_class_name

        # If only refs, return the first one (most common case)
        if refs:
            return self._resolve_ref_type(module, schema_url, refs[0])

        return "Any"

    def _resolve_any_of_property(
        self,
        module: ModuleCode,
        schema_url: str,
        prop_name: str,
        prop_schema: dict[str, Any],
    ) -> str:
        """Resolve an anyOf property type."""
        any_of = prop_schema["anyOf"]
        parts: list[str] = []
        for item in any_of:
            if "$ref" in item:
                parts.append(self._resolve_ref_type(module, schema_url, item["$ref"]))
            elif "type" in item:
                parts.append(self._json_type_to_python(item["type"]))
        if len(parts) == 1:
            return parts[0]
        return " | ".join(parts)

    def _resolve_one_of_property(
        self,
        module: ModuleCode,
        schema_url: str,
        prop_name: str,
        prop_schema: dict[str, Any],
    ) -> str:
        """Resolve a oneOf property type."""
        one_of = prop_schema["oneOf"]
        parts: list[str] = []
        for item in one_of:
            if "$ref" in item:
                parts.append(self._resolve_ref_type(module, schema_url, item["$ref"]))
            elif "type" in item:
                parts.append(self._json_type_to_python(item["type"]))
            elif "format" in item:
                parts.append("str")
        if len(parts) == 1:
            return parts[0]
        return " | ".join(parts)

    def _resolve_array_type(
        self,
        module: ModuleCode,
        schema_url: str,
        prop_name: str,
        prop_schema: dict[str, Any],
    ) -> str:
        """Resolve an array property type."""
        items = prop_schema.get("items")
        if items is None:
            return "list[Any]"

        # Items with allOf (e.g., array of merged objects)
        if "allOf" in items:
            item_type = self._resolve_all_of_array_items(module, schema_url, prop_name, items)
            return f"list[{item_type}]"

        # Items with $ref
        if "$ref" in items:
            item_type = self._resolve_ref_type(module, schema_url, items["$ref"])
            return f"list[{item_type}]"

        # Items with type
        if "type" in items:
            if items["type"] == "object" and "properties" in items:
                # Inline item class
                item_class_name = property_name_to_class_name(prop_name) + "Item"
                code = self._generate_dataclass(module, schema_url, item_class_name, items)
                if code:
                    module.classes.append(GeneratedClass(name=item_class_name, code=code))
                return f"list[{item_class_name}]"
            return f"list[{self._json_type_to_python(items['type'])}]"

        return "list[Any]"

    def _resolve_all_of_array_items(
        self,
        module: ModuleCode,
        schema_url: str,
        prop_name: str,
        items_schema: dict[str, Any],
    ) -> str:
        """Resolve array items that use allOf (e.g., technique documents merged with custom props)."""
        all_of = items_schema["allOf"]

        merged_props: dict[str, Any] = {}
        merged_required: list[str] = []
        base_refs: list[str] = []

        for item in all_of:
            if "$ref" in item:
                base_refs.append(item["$ref"])
            if isinstance(item, dict):
                if "properties" in item:
                    merged_props.update(item["properties"])
                if "required" in item:
                    merged_required.extend(item["required"])
                # Handle anyOf inside allOf (e.g., detector measurement items)
                if "anyOf" in item:
                    for variant in item["anyOf"]:
                        if "$ref" in variant:
                            # Resolve the ref and merge its properties
                            ref_schema_url, ref_def = parse_ref(variant["$ref"])
                            if ref_schema_url and ref_def:
                                ref_schema = self.schemas.get(ref_schema_url, {})
                                ref_def_schema = ref_schema.get("$defs", {}).get(ref_def, {})
                                if "properties" in ref_def_schema:
                                    merged_props.update(ref_def_schema["properties"])
                                if "required" in ref_def_schema:
                                    merged_required.extend(ref_def_schema["required"])

        # Generate an item class
        item_class_name = property_name_to_class_name(prop_name) + "Item"
        base_classes = [
            self._resolve_ref_type(module, schema_url, ref) for ref in base_refs
        ]

        if merged_props:
            merged = {"type": "object", "properties": merged_props}
            if merged_required:
                merged["required"] = merged_required
            code = self._generate_dataclass(
                module, schema_url, item_class_name, merged, base_classes=base_classes
            )
        elif base_classes:
            bases = ", ".join(base_classes)
            code = (
                f"@dataclass(frozen=True, kw_only=True)\n"
                f"class {item_class_name}({bases}):\n"
                f"    pass"
            )
        else:
            return "Any"

        if code:
            module.classes.append(GeneratedClass(name=item_class_name, code=code))
        return item_class_name

    # -------------------------------------------------------------------------
    # Quantity value + unit generation
    # -------------------------------------------------------------------------

    def _generate_quantity_value_type(
        self,
        module: ModuleCode,
        schema_url: str,
        quantity_ref: str,
        unit_ref: str,
    ) -> str:
        """Generate a TQuantityValue{Unit} type combining a quantity value with a unit."""
        # Resolve the base quantity value type
        base_type = self._resolve_ref_type(module, schema_url, quantity_ref)

        # Get the unit's const value
        _, unit_def_name = parse_ref(unit_ref)
        unit_schema_url = unit_ref.split("#")[0]
        try:
            canonical_unit_url = normalize_schema_url(unit_schema_url)
        except ValueError:
            return base_type

        unit_schema = self.schemas.get(canonical_unit_url, {})
        unit_def = unit_schema.get("$defs", {}).get(unit_def_name, {})
        const_value = self._extract_unit_const(unit_def)

        if const_value is None:
            return base_type

        # Generate the combined class name
        class_name = quantity_value_class_name(const_value)

        # Check if we already generated this class
        for cls in module.classes:
            if cls.name == class_name:
                return class_name

        # Also get the unit class for import
        unit_module = self._modules.get(canonical_unit_url)
        unit_class_name = None
        if unit_module and unit_def_name in unit_module.exported_names:
            unit_class_name = unit_module.exported_names[unit_def_name]
            unit_module_path = schema_url_to_module_path(canonical_unit_url)
            module.imports.append(ImportEntry(
                module=unit_module_path,
                name=unit_class_name,
            ))

        # Generate the combined class
        if unit_class_name:
            code = (
                f"@dataclass(frozen=True, kw_only=True)\n"
                f"class {class_name}({unit_class_name}, {base_type}):\n"
                f"    pass"
            )
        else:
            code = (
                f"@dataclass(frozen=True, kw_only=True)\n"
                f"class {class_name}({base_type}):\n"
                f"    unit: str = {const_value!r}"
            )

        module.classes.append(GeneratedClass(name=class_name, code=code))
        return class_name

    # -------------------------------------------------------------------------
    # ADM schema generation (top-level technique schemas)
    # -------------------------------------------------------------------------

    def _generate_adm_module(
        self,
        module: ModuleCode,
        schema_url: str,
        schema: dict[str, Any],
    ) -> None:
        """Generate the top-level ADM model module.

        ADM schemas use allOf at the root level to compose:
        1. Technique-specific properties
        2. Hierarchy references (techniqueAggregateDocument, etc.)
        3. ASM manifest
        """
        all_of = schema.get("allOf", [])
        required = set(schema.get("required", []))

        # Collect all properties and refs from allOf
        all_props: dict[str, Any] = {}
        all_required: set[str] = set(required)
        manifest_ref = None

        for item in all_of:
            if "$ref" in item:
                _, def_name = parse_ref(item["$ref"])
                if def_name == "asm":
                    manifest_ref = item["$ref"]
            if "properties" in item:
                all_props.update(item["properties"])
            if "required" in item:
                all_required.update(item["required"])

        # Generate the technique-specific aggregate document and inner classes
        # by recursively processing the properties
        # Each entry: (python_name, type_str, json_name, is_required)
        model_fields: list[tuple[str, str, str, bool]] = []

        # Add manifest field if present
        if manifest_ref or "$asm.manifest" in all_required:
            model_fields.append(("asm_manifest", "str", "$asm.manifest", True))

        for prop_name, prop_schema in all_props.items():
            if any(prop_name.startswith(p) for p in ASM_METADATA_PREFIXES):
                continue
            python_name = property_name_to_python(prop_name)
            type_str = self._resolve_property_type(module, schema_url, prop_name, prop_schema)
            is_required = prop_name in all_required
            model_fields.append((python_name, type_str, prop_name, is_required))

        # Generate the Model class
        lines = ["@dataclass(kw_only=True)", "class Model:"]
        required_lines = []
        optional_lines = []
        for python_name, type_str, json_name, is_required in model_fields:
            field_line = _field_declaration(python_name, type_str, json_name, is_required)
            if is_required:
                required_lines.append(field_line)
            else:
                optional_lines.append(field_line)

        lines.extend(required_lines)
        lines.extend(optional_lines)
        if not required_lines and not optional_lines:
            lines.append("    pass")

        module.classes.append(GeneratedClass(name="Model", code="\n".join(lines)))
        module.exported_names["Model"] = "Model"

    # -------------------------------------------------------------------------
    # $ref resolution
    # -------------------------------------------------------------------------

    def _resolve_ref_type(
        self, module: ModuleCode, current_schema_url: str, ref: str
    ) -> str:
        """Resolve a $ref to a Python type name, adding imports as needed."""
        ref_schema_url, def_name = parse_ref(ref)

        # Local reference within the same schema
        if ref_schema_url is None:
            if def_name:
                return def_name_to_class_name(def_name)
            return "Any"

        # External reference
        ref_module = self._modules.get(ref_schema_url)
        if ref_module and def_name and def_name in ref_module.exported_names:
            class_name = ref_module.exported_names[def_name]
            module_path = schema_url_to_module_path(ref_schema_url)
            module.imports.append(ImportEntry(module=module_path, name=class_name))
            return class_name

        # If we haven't generated the module yet (shouldn't happen with correct ordering),
        # or the definition doesn't exist, generate a best-guess name
        if def_name:
            class_name = def_name_to_class_name(def_name)
            # Try to add an import anyway
            module_path = schema_url_to_module_path(ref_schema_url)
            module.imports.append(ImportEntry(module=module_path, name=class_name))
            return class_name

        return "Any"

    # -------------------------------------------------------------------------
    # Utilities
    # -------------------------------------------------------------------------

    def _json_type_to_python(self, json_type: str | list[str]) -> str:
        """Convert a JSON Schema type to a Python type annotation."""
        if isinstance(json_type, list):
            types = [self._json_type_to_python(t) for t in json_type]
            return " | ".join(types)

        mapping = {
            "string": "str",
            "number": "float",
            "integer": "int",
            "boolean": "bool",
            "null": "None",
            "object": "dict[str, Any]",
            "array": "list[Any]",
        }
        return mapping.get(json_type, "Any")
